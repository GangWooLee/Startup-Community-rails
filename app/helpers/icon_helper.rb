module IconHelper
  # 아이콘 렌더링
  #
  # @param name [String, Symbol] 아이콘 이름 (kebab-case)
  # @param variant [Symbol] :outline (기본) 또는 :solid
  # @param size [String] "xs", "sm", "md" (기본), "lg", "xl"
  # @param css_class [String] 추가 CSS 클래스
  # @param aria_label [String] 접근성 레이블 (선택)
  # @return [String] SVG 태그 HTML
  #
  def icon(name, variant: :outline, size: "md", css_class: "", aria_label: nil)
    icon_name = name.to_s.underscore.tr("_", "-")
    icon_data = ICONS[icon_name.to_sym]

    unless icon_data
      Rails.logger.warn("Icon not found: #{icon_name}")
      return content_tag(:span, "[#{icon_name}]", class: "text-muted-foreground")
    end

    path_data = variant == :solid && icon_data[:solid] ? icon_data[:solid] : icon_data[:outline]

    unless path_data
      Rails.logger.warn("Variant #{variant} not found for icon: #{icon_name}")
      path_data = icon_data[:outline] || icon_data[:solid]
    end

    svg_class = "#{icon_size_class(size)} #{css_class}".strip
    stroke_width = variant == :outline ? "1.5" : "2"

    content_tag(:svg,
                class: svg_class,
                fill: variant == :solid ? "currentColor" : "none",
                viewBox: "0 0 24 24",
                stroke: variant == :outline ? "currentColor" : "none",
                "aria-hidden": aria_label.nil? ? "true" : nil,
                "aria-label": aria_label,
                role: aria_label ? "img" : nil) do
      if path_data.is_a?(Array)
        # Multiple paths
        safe_join(path_data.map do |path|
          content_tag(:path,
                      nil,
                      d: path,
                      "stroke-linecap": variant == :outline ? "round" : nil,
                      "stroke-linejoin": variant == :outline ? "round" : nil,
                      "stroke-width": variant == :outline ? stroke_width : nil)
        end)
      else
        # Single path
        content_tag(:path,
                    nil,
                    d: path_data,
                    "stroke-linecap": variant == :outline ? "round" : nil,
                    "stroke-linejoin": variant == :outline ? "round" : nil,
                    "stroke-width": variant == :outline ? stroke_width : nil)
      end
    end
  end

  private

  # Size별 Tailwind 클래스
  def icon_size_class(size)
    case size.to_s
    when "xs" then "w-3 h-3"
    when "sm" then "w-4 h-4"
    when "md" then "w-5 h-5"
    when "lg" then "w-6 h-6"
    when "xl" then "w-8 h-8"
    when "2xl" then "w-10 h-10"
    else "w-5 h-5" # default
    end
  end

